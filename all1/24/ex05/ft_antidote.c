int		ft_antidote(int i, int j, int k)
{
	int tmp;

	if (i > j)
	{
		tmp = i;
		i = j;
		j = tmp;
	}
	if (i > k)
	{
		tmp = i;
		i = k;
		k = tmp;
	}
	if (j > k)
	{
		tmp = k;
		k = j;
		j = tmp;
	}
	return (j);
}
